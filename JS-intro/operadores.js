// incremento -> importante colocar o incremento antes da variavel, para qie a impressão seja dada
let numero = 10

console.log(++numero);
console.log(++numero);
console.log(++numero);

// Decremento
console.log(--numero);

// comparação Simples -> usa ==
console.log(5=="5"); // retorna true

// comparação  Rigorosa -> além d comparar o valor, ela também considera se o tipo de dado é o mesmo
console.log(5==="5"); // retorna false

// !== -> diferente rigorosa 
// != -> diferente simples 
// && -> and
// || -> or - em uma comparação com duas variaveis, 

console.log(10 > 5 && 8 < 4);
console.log(10 > 5 || 8 < 4);

//comparador de negação -> !
console.log(!8 > 5); // aqui eu uso o ! para dizer que oito não é maior que cinco, retornando um resultado falso

